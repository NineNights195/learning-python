x = float(input("Enter first number: "))
y = float(input("Enter second number: "))
statement = input("Enter statement: ")

if statement == '+':
    print(f"Output: {x+y}")
if statement == '-':
    print(f"Output: {x-y}")
if statement == '*':
    print(f"Output: {x*y}")
if statement == '/':
    print(f"Output: {x/y}")
